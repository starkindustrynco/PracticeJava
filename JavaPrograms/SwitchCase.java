public class SwitchCase{
	public static void main(String[] args){
        int i = 6 ;
        switch(i){
        	default : System.out.println("Shah");
        	
        	case 1 : System.out.println("Deepika");
        	
        	case 2 : System.out.println("Subhash");
        	
        	case 3 : System.out.println("Katrina");
        	
        	case 4 : System.out.println("Akshay");
        	
        	case 5 : System.out.println("Rajni");
        	
        }
	}
}



